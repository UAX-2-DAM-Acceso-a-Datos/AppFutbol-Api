package com.uax.accesodatos.AppFutbolApi.dto.entrenadores;

public class Career{
    public Team team;
    public String start;
    public String end;
    
    
	public Team getTeam() {
		return team;
	}
	public void setTeam(Team team) {
		this.team = team;
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
    
    
}
